package Module4;

public class ClassInterger {
    public int a = 2;
    public int b = 5;
    public int c = 9;
    public int d;
    public void kali(){
        d = a * b * c;
        System.out.println(d);
    }
    public void tambah(){
        d = a + b + c;
        System.out.println(d);
    }
    public void kurang(){
        d = a - b - c;
        System.out.println(d);
    }
    public void bagi(){
        d = a / b / c;
        System.out.println(d);
    }
    public void rata_rata(){
        d = (a + b + c) / 3;
        System.out.println(d);
    }
}
